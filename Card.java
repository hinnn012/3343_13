package Big_Two;

public class Card {

	private String face;	//e.g. ace to king
	private String suit;	//e.g. diamond to spade
	
	public Card(String cFace, String cSuit) {
		
		cFace = face;
		cSuit = suit;
		
		
	}
	
	public String getFace() {
		
		return face;
		
	}
	
	public String getSuit() {
		
		return suit;
		
	}
	
}

